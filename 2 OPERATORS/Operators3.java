public class Operators3 {
    public static void main(String[] args) {
        // max(int,type of a,type of b)
        byte a=10;
        byte b=20;
        byte c=(byte)(a+b);

        System.out.println(c);

        byte x=10;
        x++;
        System.out.println(x);
        //internal type casting will be performed

        byte y=10;
        byte y=y+1;
        System.out.println(y);
    }
}







