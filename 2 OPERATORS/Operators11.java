public class Operators11 {
    public static void main(String[] args) {
        //Bitwise Operators
        
        System.out.println(true & false); //iff both arguments are true.
        System.out.println(true | false); //iff atleast one argument is true.
        System.out.println(true ^ false); //iff both arguments are different then is true.

        System.out.println(4&5);
        System.out.println(4|5);
        System.out.println(4^5);

        //complement operator
        // System.out.println(~true);
        System.out.println(~4); //5

        //Boolean complement operator
        // System.out.println(!4);
        System.out.println(!false);
    }
}
