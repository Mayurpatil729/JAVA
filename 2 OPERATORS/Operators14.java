public class Operators14 {
    public static void main(String[] args) {
        // Type Caste Operators
        // 1. Implicit Type Casting
        // compiler covert char to int automatically by implicit typecasting.
        // smaller--> bigger . it is known as widening or upcasting
        // No loss of information.

        int x = 'a';
        System.out.println(x);
        // 97

        double d = 10;
        System.out.println(d);
        // 10.0
    }

}
