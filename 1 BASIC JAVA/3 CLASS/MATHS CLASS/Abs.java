public class Abs {
    public static void main(String[] args) {
        System.out.println(15);
        System.out.println(-5);

        double x=23.5;
        System.out.println(Math.ceil(x));
        System.out.println(Math.floor(x));

        System.out.println(Math.exp(5));

        System.out.println(Math.log(50));
        System.out.println(Math.log10(50));

        System.out.println("Max between 50 and 30 : "+Math.max(50,30));
        System.out.println("Min between 50 and 30 : "+Math.min(50,30));

        System.out.println("2^6 :"+Math.pow(2,6));
    }
}
